package com.shadhini.java.tryouts.basics;

/**
 * About Java `for` loops
 */
public class ForLoops {

    public static void main(String[] args) {

        for (int i=0; i<5; i++)
            System.out.println(i);
        System.out.println("------------------------------------");
        for (int j=1; j<=5; j++)
            System.out.println(j);
        System.out.println("------------------------------------");
        for (int k=5; k>0; k--)
            System.out.println(k);
        System.out.println("------------------------------------");
        for (int l=4; l>=0; l--)
            System.out.println(l);
    }
}
