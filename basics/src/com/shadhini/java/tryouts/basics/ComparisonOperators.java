package com.shadhini.java.tryouts.basics;

/**
 * About Java comparison operators.
 */
public class ComparisonOperators {

    public static void main(String[] args) {
        int x = 1;
        int y = 1;
        int z = 2;
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(z >= y);
    }
}
